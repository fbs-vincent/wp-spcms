            <p>
            The Philippine Medical Association recorded the existence of SAN PABLO CITY MEDICAL ASSOCIATION officially on January
8, 1947. It was on September 30, 1966 that the association was applied for a corporate status. Four months later, on January 10, 1967,
with the approval of the incorporation under the corporate name “SAN PABLO CITY MEDICAL SOCIETY, INC” by the Securities and
Exchange Commission, the SPCMS attained a legal personality.
            </p>
            <p>
            Before 1947, the physicians of SPC were associated with other medical groups, being members of the Laguna Medical
Association based in Sta. Cruz Laguna and that Manila Medical Society. With a deep sense of unity and camaraderie, the leading
medical practitioners of that time organized the San Pablo City Medical Association with twenty-three charter members; it was headed
by Dr. Cirilio Cauyan as Charter president and Dr. Archimedes Brion as Secretary.
            </p>
            <p>
            Within its 66 years of existence (1947-2013). Thirty-nine past presidents have served the society, having their own merits and
achievements. To note, the 17 th president, Dr. Petronio P. Alava received a lot of donation as the future site of the medical society
building and also became the first elected PMA District Governor of Southern Tagalog Region. Its 24 th president, Dr.Melecio V. Marilim
was amongst the founding members of the SOUTHERN TAGALOG ASSOCIATION OF MEDICAL PRESIDENTS (STAMP) which
eventually was change to Sth. Tagalog Association of Medical PRACTITIONERS in 2004 under the term of Dr. Emmanuel D. Loyola
(33 rd President). The Philippine Medical Association (PMA) recognizes the San Pablo City Medical Society as one of the oldest existing
local Medical Association in the country.
            </p>
            <p>
            The <span class="bold">1<sup>st</sup> decade (1947-1957)</span> was marked by rapid change and development in the city of San Pablo. It became the center of
commerce, banking, transportation and education in the southern tagalog region. The Medical Association grew in prominence and
became the venue for continuing Medical Education for its members. Esteemed Specialists from Manila were invited as speakers in the
regular scientific fora.The members provided outmost medical care and health services to the city and the adjacent municipalities and
provinces.
            </p>
            <p>
            <span class="bold">2<sup>nd</sup> decade (1958-1968)</span> the San Pablo City Hospital became the center for training for the local Physicians, ably handled by
the then Chief of Hospital Dr. Bonifacio Reyes. In the same decade, Dr. Delfin Bautista set up the Rehabilitation Center for
Orthopedic patients at the City Hospital (1959). In 1961, the San Pablo Puericulture center was set up through the efforts of the then
association President, Dr. Fernando Bautista. In 1966, the first private Hospital in San Pablo- San Pablo Doctors Hospital was founded
by former San Pablo Medical Association President Dr. Archimedes Brion. This further improved the level of medical care in the city.
San Pablo Physicians who had their specialty training abroad started their careers in the said hospital. In 1967, the second private
hospital in San Pablo City, the Community General Hospital of San Pablo City was established. It further improved and modernized
medical practice in San Pablo.
            </p>
            <p>
            The <span class="bold">3<sup>rd</sup> Decade (1969-1979)</span>, the first female president of SPMS was elected in the person of Dr.
Virginia Reyes –Estrada. Amendments to the Society’s Constitution and By-laws were presented during this period. Several private
hospitals opened in San Pablo during the 1970”s. These are the following: SPC Medical Center, Hospital of St. Gerard, Bel-Cast Family
Hospital, Hospital of Sts. Francis and Paul, Immaculate Concepcion Hospital.
            </p>
            <p>
            During the <span class="bold">4<sup>th</sup> Decade (1980-1990)</span>, Dr. Oscar Brion
worked for the standardization of charges among surgeons and private hospitals in the city. A weekly (Medi-balita) was published in a
local newspaper (The Barangay) to inform the local citizenry of the current advances in medicine and patient care.The term of Dr.
Adoracion Alava focused on providing training of out –of- school youths. In coordination with the Department of Education, basic
training for would be hospital/nursing aides were started. Health Education and livelihood program for the Barrio-folks were the legacy
left by her term. Dr. Medwin Mercado started the “ADOPT A BARANGAY program. Professional fees were standardized. Members
were encouraged to be part of the Society’s various sports teams.
            </p>
            <p>
            The <span class="bold">5<sup>th</sup> Decade (1991-2001)</span> the decade was welcomed by a series of
natural calamities hitting the country one after the other. The society actively sent donations and volunteers to the affected areas. In
1993, the City Government of San Pablo officially recognized the San Pablo Medical Society as a Non-Governmental Organization.
Thus, our society gained a seat at San Pablo City’s Health Council. In 1997, San Pablo Medical Society celebrated her Golden Year.
PMA President Homobono Calleja, inducted Dr. Angelito Benjamin-Belen as the 29 th President of the SPMS. Computer literacy
program for all members of the San Pablo Medical Society was a highlight of the year. During the term of Dr. Cynthia Sanchez, focus
was on the upliftment of the lives of senior citizens in the city. Regular medical missions and education in depressed areas were held.
            </p>
            <p>
            On the <span class="bold">6<sup>th</sup> Decade (2002-2012)</span>, San Pablo Medical Society was awarded her first Icasiano Award (third place) during the term of Dr.
Amor Awayan . During the term of Dr. Dionisio Gutierrez, the official name of the society became “The San Pablo City Medical

Society” The official logo and banner of the society was likewise redesigned by Dr. Raisa Francisco-Iley.
            </p>
           <p>
           The <span class="bold">7<sup>th</sup> Decade (2013-2019)</span>,
started with another Icasiano Award during the term of Dr. Haydee Viray-Sarmiento. The new leaders of the society were now more
focused in Nation building. Activities were based on its impact to the overall health upliftment of the Filipinos, particularly in their
localities. Through the creative efforts of Dr. Rose C. Shellshear, SPCMS was finally awarded the much coveted Icasiano award (first
place) last 2014.
           </p>
          <p>
          Awards and recognition are always welcome addition to a medical society’s illustrious history. But at times, the acid test of what
a society is worth comes from the most unexpected circumstances. Such was the experience of Dr. Randy Melo who during his term as
society president (2019-2020) had to face several natural disasters like devastating typhoons and the massive eruption of Taal volcano
in January of 2020. The society was ever ready to respond at a moment’s notice and was among the key organizations to come to the
aid of disaster victims wherever any assistance in whatever form was needed. Dr. Melo garnered for the SPCMS another Icasiano
award and then turned over the reins of the society the following year to his successor Dr. Edgardo Noblejas Jr. (2020-2021) who
became the “Pandemic President” as Covid 19 created problems in all aspects of daily life and challenged everyone to cope and
survive in the so-called “new normal.” The San Pablo City Medical Society took up the challenge of life in the time of Covid and not only
continued to pursue its goals towards quality health care for the city but more importantly, became a vital force in the continuing fight
against the pandemic. The resilience and the sense of service beyond the call of duty equally defines and distinguishes the San Pablo
City Medical Society as one worthy of emulation.
          </p>
          <p>
          It is said that our vision of the future is rooted in our image of the past. A great lesson was derived from our founders as we look
back at our humble beginnings: they saw and approached the future with a VISION, a sense of MISSION, COMPASSION and
UNIVERSAL HEALTH CARE for all Filipinos. We take all these to heart as the San Pablo City Medical Society prepares for what the
future holds in store.
          </p>
